    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        // TODO add your handling code here:
        if (DAO.check(Application.ID)){
            JOptionPane.showMessageDialog(this, "You have already signed in this class!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            DAO.add();
            JOptionPane.showMessageDialog(this, "Succesfully asigned!", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
        showCourse();
    }//GEN-LAST:event_AddButtonActionPerformed

    private void MyCourseCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MyCourseCheckActionPerformed
        // TODO add your handling code here:
        if (MyCourseCheck.isSelected()){

            DefaultTableModel model = (DefaultTableModel) tblCourse.getModel();
            while(model.getRowCount() > 0)
            {
                model.removeRow(0);
            }

            showCourse2();

        } else {
            showCourse();
        }
    }//GEN-LAST:event_MyCourseCheckActionPerformed

    private void RemoveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveButtonActionPerformed
        // TODO add your handling code here:
        if (!DAO.check(Application.ID)){
            JOptionPane.showMessageDialog(this, "You are not in this class!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            DAO.remove();
            JOptionPane.showMessageDialog(this, "Succesfully removed!", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
        showCourse();
    }//GEN-LAST:event_RemoveButtonActionPerformed

