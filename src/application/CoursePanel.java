    private void SearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchButtonActionPerformed
        // TODO add your handling code here:
        String searchString = SearchField.getText();
        tableModel.setRowCount(0);

        if (searchString.length()>0){

            if (DAO.search(searchString) == false){
                JOptionPane.showMessageDialog(this, "No course found!", "Error", JOptionPane.INFORMATION_MESSAGE);
                //                TextID.setBackground(new Color(255, 123, 123));
            } else {

                SearchField.setBackground(null);
                CourseList = DAO.findByFullName(searchString);

                CourseList.forEach(Course -> {
                    tableModel.addRow(new Object[] { Course.getCourseName(),
                        Course.getTutor(), Course.getCredits(), Course.getClassCode(), Course.getCourseCode(), Course.getAmount()});
            });

        }
        }
    }//GEN-LAST:event_SearchButtonActionPerformed

    private void SearchFieldFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_SearchFieldFocusGained
        // TODO add your handling code here:
        if (SearchField.getText().equals("Code/Name")){
            SearchField.setText(null);
            SearchField.requestFocus();
            placeholderRemove(SearchField);
        }
    }//GEN-LAST:event_SearchFieldFocusGained

    private void SearchFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_SearchFieldFocusLost
        // TODO add your handling code here:
        if (SearchField.getText().length()==0){

            placeholder(SearchField);
            SearchField.setText("Code/Name");

        }
    }//GEN-LAST:event_SearchFieldFocusLost

