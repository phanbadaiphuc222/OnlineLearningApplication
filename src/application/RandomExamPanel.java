    public void showQuestion() {

        QuestionList = DAO.findRandomQuestion(Integer.parseInt(Mark3.getText()), Integer.parseInt(Mark4.getText()), Integer.parseInt(Mark5.getText()));

        tableModel.setRowCount(0);

        QuestionList.forEach(Question -> {
            tableModel.addRow(new Object[]{
                Question.getQuestions(), Question.getId(), Question.getGrade()});
        });
    }

