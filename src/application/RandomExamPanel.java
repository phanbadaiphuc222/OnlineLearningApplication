    private void ImplementButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ImplementButtonActionPerformed
        // TODO add your handling code here:
        int a = Integer.parseInt(Mark3.getText());
        int b = Integer.parseInt(Mark4.getText());
        int c = Integer.parseInt(Mark5.getText());
        
        boolean res3 = false;
        boolean res4 = false;
        boolean res5 = false;

        if (a > DAO.totalQuesiton("3")) {
//            JOptionPane.showMessageDialog(this, "Not enough question for 3 marks!", "Error", JOptionPane.ERROR_MESSAGE);
            Mark3.setBackground(new Color(255, 123, 123));
            Warn3.setText("Not enough questions!");
        } else {
            Mark3.setBackground(new Color(255, 255, 255));
            Warn3.setText("");
            res3 = true;
        }
        if (b > DAO.totalQuesiton("4")) {
//            JOptionPane.showMessageDialog(this, "Not enough question for 4 marks!", "Error", JOptionPane.ERROR_MESSAGE);
            Mark4.setBackground(new Color(255, 123, 123));
            Warn4.setText("Not enough questions!");
        } else {
            Mark4.setBackground(new Color(255, 255, 255));
            Warn4.setText("");
            res4 = true;
        }
        if (c > DAO.totalQuesiton("5")) {
//            JOptionPane.showMessageDialog(this, "Not enough question for 5 marks!", "Error", JOptionPane.ERROR_MESSAGE);
            Mark5.setBackground(new Color(255, 123, 123));
            Warn5.setText("Not enough questions!");
        } else {
            Mark5.setBackground(new Color(255, 255, 255));
            Warn5.setText("");
            res5 = true;
        }
        if (res3 == true && res4 == true && res5 == true) {
                int total = Integer.parseInt(Mark3.getText()) + Integer.parseInt(Mark4.getText()) + Integer.parseInt(Mark5.getText());
                int mark = Integer.parseInt(Mark3.getText()) * 3 + Integer.parseInt(Mark4.getText()) * 4 + Integer.parseInt(Mark5.getText()) * 5;
                NumberQuestionField.setText(String.valueOf(total));
                TotalMarkField.setText(String.valueOf(mark));
                DAO.eraseQuestion(Application.ExamID);
                showQuestion();
        }
    }//GEN-LAST:event_ImplementButtonActionPerformed

